/**
 * 获取省份列表
 */
export function getProvinces() {
  return regions.map(p => p.name)
}

/**
 * 根据省份获取城市列表
 */
export function getCities(provinceName) {
  const province = regions.find(p => p.name === provinceName)
  if (!province) return []
  return province.children.map(c => c.name)
}

/**
 * 根据省份和城市获取区县列表
 */
export function getDistricts(provinceName, cityName) {
  const province = regions.find(p => p.name === provinceName)
  if (!province) return []
  const city = province.children.find(c => c.name === cityName)
  if (!city) return []
  return city.districts
}

/**
 * 获取多列picker数据
 * 返回格式: [provinces, cities, districts]
 */
export function getRegionColumns(provinceIdx = 0, cityIdx = 0) {
  const provinces = getProvinces()
  const provinceName = provinces[provinceIdx] || provinces[0]
  const cities = getCities(provinceName)
  const cityName = cities[cityIdx] || cities[0]
  const districts = getDistricts(provinceName, cityName)
  return [provinces, cities, districts]
}

